#include <iostream>
using namespace std;

int main()
{
    char vowel;
    cin >> vowel;
    switch (vowel)
    {
    case 'a':

    case 'e':

    case 'i':

    case 'o':

    case 'u':
        cout << "Vowel";
        break;
    default:
        cout << "Consonant";
        break;
    }
    return 0;
}